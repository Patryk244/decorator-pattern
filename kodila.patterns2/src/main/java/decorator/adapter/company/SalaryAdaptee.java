package decorator.adapter.company;

import decorator.adapter.company.newhrsystem.CompanySalaryProcessor;
import decorator.adapter.company.newhrsystem.Employee;
import decorator.adapter.company.newhrsystem.SalaryProcessor;

import java.math.BigDecimal;
import java.util.List;

public class SalaryAdaptee implements SalaryProcessor {
    @Override
    public BigDecimal calculateSalaries(List<Employee> employees) {
        CompanySalaryProcessor theProcessor = new CompanySalaryProcessor();
        return theProcessor.calculateSalaries(employees);
    }
}
